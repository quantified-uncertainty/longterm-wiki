/**
 * Agent Workspace Command Handlers — Local multi-agent directory management
 *
 * Manages the `lw/` directory structure for running multiple Claude Code agents
 * in parallel, each with its own clone and port configuration.
 *
 * Usage:
 *   crux agent-workspace setup <N>       Initialize agent slot N (clone + .env + .agent-slot)
 *   crux agent-workspace sync-env        Regenerate .env for all agent slots from .env.base
 *   crux agent-workspace list            Show all agent slots with branch/PR/status info
 *   crux agent-workspace clean [N]       Remove idle agent slots (on main, no changes)
 */

import { existsSync, readFileSync, writeFileSync, readdirSync, statSync } from 'fs';
import { join, dirname } from 'path';
import { execSync } from 'child_process';
import type { CommandOptions as BaseOptions, CommandResult } from '../lib/command-types.ts';
import { PROJECT_ROOT } from '../lib/content-types.ts';
import { getColors } from '../lib/output.ts';

interface CommandOptions extends BaseOptions {
  ci?: boolean;
  json?: boolean;
  force?: boolean;
}

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

/** Resolve the `lw/` parent directory (sibling to the repo) */
function getLwDir(): string {
  // PROJECT_ROOT is the repo root (e.g., /Users/.../lw/a3 or /Users/.../longterm-wiki)
  // The lw/ dir is the parent when running from an agent slot,
  // or needs to be explicitly at the same level as the repo.
  const parent = dirname(PROJECT_ROOT);
  const dirName = PROJECT_ROOT.split('/').pop() || '';

  // If we're already inside lw/aN or lw/main, parent is lw/
  if (/^(a\d+|main)$/.test(dirName) && parent.endsWith('/lw')) {
    return parent;
  }

  // Otherwise, lw/ is a sibling directory
  return join(parent, 'lw');
}

function getGitRemoteUrl(): string {
  try {
    return execSync('git remote get-url origin', { cwd: PROJECT_ROOT, encoding: 'utf-8' }).trim();
  } catch {
    return 'git@github.com:quantified-uncertainty/longterm-wiki.git';
  }
}

/** Read the slot number from a directory's .agent-slot file */
function readSlot(dir: string): number | null {
  try {
    const raw = readFileSync(join(dir, '.agent-slot'), 'utf-8').trim();
    const n = parseInt(raw, 10);
    return Number.isInteger(n) && n > 0 ? n : null;
  } catch {
    return null;
  }
}

/** Find all agent slot directories under lw/ */
function findSlotDirs(lwDir: string): Array<{ dir: string; slot: number }> {
  if (!existsSync(lwDir)) return [];

  const entries = readdirSync(lwDir).filter((name) => /^a\d+$/.test(name));
  const results: Array<{ dir: string; slot: number }> = [];

  for (const name of entries) {
    const dir = join(lwDir, name);
    if (!statSync(dir).isDirectory()) continue;
    const slot = readSlot(dir);
    if (slot !== null) {
      results.push({ dir, slot });
    }
  }

  return results.sort((a, b) => a.slot - b.slot);
}

/** Generate .env content from .env.base + port overrides for a given slot */
function generateEnv(envBasePath: string, slot: number): string {
  const base = readFileSync(envBasePath, 'utf-8');

  // Remove any existing port-related lines from base
  const filtered = base
    .split('\n')
    .filter((line) => {
      const key = line.split('=')[0]?.trim();
      return !['DEV_PORT', 'PORT', 'LONGTERMWIKI_SERVER_URL', 'PLAYWRIGHT_BASE_URL'].includes(key);
    })
    .join('\n');

  const portBlock = [
    '',
    `# Agent ${slot} port config (auto-generated by crux agent-workspace)`,
    `DEV_PORT=${3010 + slot}`,
    `PORT=${3110 + slot}`,
    `LONGTERMWIKI_SERVER_URL=http://localhost:${3110 + slot}`,
    `PLAYWRIGHT_BASE_URL=http://localhost:${3010 + slot}`,
    '',
  ].join('\n');

  return filtered.trimEnd() + '\n' + portBlock;
}

/** Run a git command in a directory and return trimmed output */
function git(dir: string, ...args: string[]): string {
  try {
    return execSync(`git ${args.join(' ')}`, { cwd: dir, encoding: 'utf-8', timeout: 10000 }).trim();
  } catch {
    return '';
  }
}

// ---------------------------------------------------------------------------
// setup
// ---------------------------------------------------------------------------

async function setup(args: string[], options: CommandOptions): Promise<CommandResult> {
  const slotArg = args.find((a) => !a.startsWith('--'));
  if (!slotArg) {
    return {
      exitCode: 1,
      output: `Usage: crux agent-workspace setup <N>

  Initialize agent slot N with a fresh clone, .env, and .agent-slot file.

  Creates lw/aN/ with:
    - Git clone of the repository
    - .agent-slot file (contains the slot number)
    - .env generated from lw/.env.base with per-agent port overrides

  Port assignments (deterministic from slot number):
    Agent N → Next.js on port 301N, wiki-server on port 311N

Options:
  --force    Re-initialize even if the slot directory already exists`,
    };
  }

  const slot = parseInt(slotArg, 10);
  if (!Number.isInteger(slot) || slot < 1 || slot > 99) {
    return { exitCode: 1, output: 'Error: Slot number must be an integer between 1 and 99' };
  }

  const lwDir = getLwDir();
  const slotDir = join(lwDir, `a${slot}`);
  const envBasePath = join(lwDir, '.env.base');

  // Ensure lw/ exists
  if (!existsSync(lwDir)) {
    execSync(`mkdir -p "${lwDir}"`);
  }

  // Check for .env.base
  if (!existsSync(envBasePath)) {
    return {
      exitCode: 1,
      output: `Error: ${envBasePath} not found.\n\nCreate it first by copying your .env (without port-specific lines):\n  cp ${PROJECT_ROOT}/.env ${envBasePath}`,
    };
  }

  // Clone if needed
  if (existsSync(slotDir)) {
    if (!options.force) {
      const existingSlot = readSlot(slotDir);
      if (existingSlot !== null) {
        return {
          exitCode: 0,
          output: `Slot a${slot} already exists at ${slotDir}. Use --force to re-initialize .env and .agent-slot.`,
        };
      }
    }
  } else {
    const remote = getGitRemoteUrl();
    console.error(`Cloning ${remote} → ${slotDir}...`);
    execSync(`git clone "${remote}" "${slotDir}"`, { stdio: 'inherit', timeout: 300000 });
  }

  // Write .agent-slot
  writeFileSync(join(slotDir, '.agent-slot'), String(slot) + '\n');

  // Generate .env
  const envContent = generateEnv(envBasePath, slot);
  writeFileSync(join(slotDir, '.env'), envContent);

  let output = `✓ Agent slot a${slot} ready at ${slotDir}\n`;
  output += `  Next.js:     http://localhost:${3010 + slot}\n`;
  output += `  Wiki-server: http://localhost:${3110 + slot}\n`;
  output += `  .env:        generated from .env.base\n`;
  output += `\nNext steps:\n`;
  output += `  cd ${slotDir} && pnpm install && pnpm setup:quick\n`;

  return { exitCode: 0, output };
}

// ---------------------------------------------------------------------------
// sync-env
// ---------------------------------------------------------------------------

async function syncEnv(_args: string[], _options: CommandOptions): Promise<CommandResult> {
  const lwDir = getLwDir();
  const envBasePath = join(lwDir, '.env.base');

  if (!existsSync(envBasePath)) {
    return {
      exitCode: 1,
      output: `Error: ${envBasePath} not found.`,
    };
  }

  const slots = findSlotDirs(lwDir);
  if (slots.length === 0) {
    return { exitCode: 0, output: 'No agent slots found.' };
  }

  const lines: string[] = [];
  for (const { dir, slot } of slots) {
    const envContent = generateEnv(envBasePath, slot);
    writeFileSync(join(dir, '.env'), envContent);
    lines.push(`  ✓ a${slot}/.env regenerated (ports ${3010 + slot}/${3110 + slot})`);
  }

  return {
    exitCode: 0,
    output: `Synced .env for ${slots.length} agent slot(s) from ${envBasePath}:\n${lines.join('\n')}`,
  };
}

// ---------------------------------------------------------------------------
// list
// ---------------------------------------------------------------------------

async function list(_args: string[], options: CommandOptions): Promise<CommandResult> {
  const lwDir = getLwDir();
  const slots = findSlotDirs(lwDir);

  // Also check for lw/main
  const mainDir = join(lwDir, 'main');
  const hasMain = existsSync(mainDir) && statSync(mainDir).isDirectory();

  if (slots.length === 0 && !hasMain) {
    return { exitCode: 0, output: `No agent workspace found at ${lwDir}` };
  }

  interface SlotInfo {
    slot: string;
    branch: string;
    dirty: number;
    pr: string;
    ports: string;
  }

  const infos: SlotInfo[] = [];

  if (hasMain) {
    const branch = git(mainDir, 'branch', '--show-current') || 'detached';
    const mainPorcelain = git(mainDir, 'status', '--porcelain');
    const dirty = mainPorcelain ? mainPorcelain.split('\n').filter(Boolean).length : 0;
    infos.push({ slot: 'main', branch, dirty, pr: '', ports: 'default' });
  }

  for (const { dir, slot } of slots) {
    const branch = git(dir, 'branch', '--show-current') || 'detached';
    const porcelain = git(dir, 'status', '--porcelain');
    const dirty = porcelain ? porcelain.split('\n').filter(Boolean).length : 0;

    // Try to get PR number (best-effort, don't fail if gh isn't available)
    let pr = '';
    try {
      const prJson = execSync(
        `gh pr view --json number,state --jq '"#" + (.number|tostring) + " " + .state' 2>/dev/null`,
        { cwd: dir, encoding: 'utf-8', timeout: 10000 },
      ).trim();
      if (prJson) pr = prJson;
    } catch {
      // No PR or gh not available
    }

    infos.push({
      slot: `a${slot}`,
      branch,
      dirty,
      pr,
      ports: `${3010 + slot}/${3110 + slot}`,
    });
  }

  if (options.json) {
    return { exitCode: 0, output: JSON.stringify(infos, null, 2) };
  }

  const c = getColors(options.ci);

  let output = `Agent Workspace (${lwDir})\n\n`;
  output += `  ${'Slot'.padEnd(6)} ${'Branch'.padEnd(35)} ${'Dirty'.padEnd(6)} ${'PR'.padEnd(15)} Ports\n`;
  output += `  ${'─'.repeat(6)} ${'─'.repeat(35)} ${'─'.repeat(6)} ${'─'.repeat(15)} ${'─'.repeat(12)}\n`;

  for (const info of infos) {
    const dirtyStr = info.dirty > 0 ? `${info.dirty}` : '—';
    const line = `  ${info.slot.padEnd(6)} ${info.branch.padEnd(35)} ${dirtyStr.padEnd(6)} ${(info.pr || '—').padEnd(15)} ${info.ports}`;

    // Color: idle (main+clean) → dim, active (branch or dirty) → green, merged PR → yellow
    if (info.pr.includes('MERGED')) {
      output += `${c.yellow}${line}${c.reset}\n`;
    } else if (info.branch !== 'main' || info.dirty > 0) {
      output += `${c.green}${line}${c.reset}\n`;
    } else {
      output += `${c.dim}${line}${c.reset}\n`;
    }
  }

  // Branch collision detection: warn if two slots share a non-main branch
  const branchMap = new Map<string, string[]>();
  for (const info of infos) {
    if (info.branch !== 'main' && info.branch !== 'detached') {
      const existing = branchMap.get(info.branch) || [];
      existing.push(info.slot);
      branchMap.set(info.branch, existing);
    }
  }
  for (const [branch, slots] of branchMap) {
    if (slots.length > 1) {
      output += `\n${c.yellow}⚠ Branch collision: ${branch} is checked out in ${slots.join(', ')}${c.reset}`;
    }
  }

  return { exitCode: 0, output };
}

// ---------------------------------------------------------------------------
// clean
// ---------------------------------------------------------------------------

async function clean(args: string[], options: CommandOptions): Promise<CommandResult> {
  const lwDir = getLwDir();
  const slots = findSlotDirs(lwDir);

  const targetSlot = args.find((a) => !a.startsWith('--'));
  const toCheck = targetSlot
    ? slots.filter((s) => s.slot === parseInt(targetSlot, 10))
    : slots;

  if (toCheck.length === 0) {
    return { exitCode: 0, output: 'No agent slots to clean.' };
  }

  const cleaned: string[] = [];
  const skipped: string[] = [];

  for (const { dir, slot } of toCheck) {
    const branch = git(dir, 'branch', '--show-current');
    const porcelain = git(dir, 'status', '--porcelain');
    const dirty = porcelain ? porcelain.split('\n').filter(Boolean).length : 0;

    if (branch !== 'main' || dirty > 0) {
      const reasons: string[] = [];
      if (branch !== 'main') reasons.push(`on branch ${branch}`);
      if (dirty > 0) reasons.push(`${dirty} uncommitted changes`);
      skipped.push(`  a${slot}: ${reasons.join(', ')}`);
      continue;
    }

    if (!options.force) {
      skipped.push(`  a${slot}: idle (use --force to remove)`);
      continue;
    }

    execSync(`rm -rf "${dir}"`);
    cleaned.push(`  ✓ a${slot} removed`);
  }

  let output = '';
  if (cleaned.length > 0) {
    output += `Cleaned ${cleaned.length} slot(s):\n${cleaned.join('\n')}\n`;
  }
  if (skipped.length > 0) {
    output += `Skipped ${skipped.length} slot(s):\n${skipped.join('\n')}\n`;
  }

  return { exitCode: 0, output };
}

// ---------------------------------------------------------------------------
// open
// ---------------------------------------------------------------------------

async function open(args: string[], _options: CommandOptions): Promise<CommandResult> {
  const slotArg = args.find((a) => !a.startsWith('--'));
  if (!slotArg) {
    return { exitCode: 1, output: 'Usage: crux agent-workspace open <N> [--claude]\n\nOpens a tmux window at the agent slot directory.' };
  }

  if (!process.env.TMUX) {
    return { exitCode: 1, output: 'Error: Not inside a tmux session. Run this from within tmux.' };
  }

  const slot = parseInt(slotArg, 10);
  if (!Number.isInteger(slot) || slot < 1) {
    return { exitCode: 1, output: 'Error: Slot number must be a positive integer.' };
  }

  const lwDir = getLwDir();
  const slotDir = join(lwDir, `a${slot}`);

  if (!existsSync(slotDir)) {
    return { exitCode: 1, output: `Error: Slot a${slot} does not exist at ${slotDir}. Run setup first.` };
  }

  const withClaude = args.includes('--claude');
  const windowName = `A${slot}`;

  try {
    if (withClaude) {
      execSync(`tmux new-window -n "${windowName}" -c "${slotDir}" "claude"`, { stdio: 'inherit' });
    } else {
      execSync(`tmux new-window -n "${windowName}" -c "${slotDir}"`, { stdio: 'inherit' });
    }
    return { exitCode: 0, output: `Opened tmux window ${windowName} at ${slotDir}${withClaude ? ' (with claude)' : ''}` };
  } catch (e) {
    return { exitCode: 1, output: `Error opening tmux window: ${e instanceof Error ? e.message : String(e)}` };
  }
}

// ---------------------------------------------------------------------------
// refresh
// ---------------------------------------------------------------------------

async function refresh(_args: string[], _options: CommandOptions): Promise<CommandResult> {
  const lwDir = getLwDir();
  const slots = findSlotDirs(lwDir);

  if (slots.length === 0) {
    return { exitCode: 0, output: 'No agent slots found.' };
  }

  const updated: string[] = [];
  const skipped: string[] = [];

  for (const { dir, slot } of slots) {
    const branch = git(dir, 'branch', '--show-current');
    if (branch !== 'main') {
      skipped.push(`  a${slot}: on branch ${branch}`);
      continue;
    }

    const porcelain = git(dir, 'status', '--porcelain');
    if (porcelain) {
      skipped.push(`  a${slot}: has uncommitted changes`);
      continue;
    }

    try {
      execSync('git pull origin main', { cwd: dir, encoding: 'utf-8', timeout: 30000, stdio: 'pipe' });
      updated.push(`  ✓ a${slot}: pulled latest main`);
    } catch (e) {
      skipped.push(`  a${slot}: git pull failed (${e instanceof Error ? e.message.split('\n')[0] : 'unknown error'})`);
    }
  }

  let output = '';
  if (updated.length > 0) {
    output += `Updated ${updated.length} slot(s):\n${updated.join('\n')}\n`;
  }
  if (skipped.length > 0) {
    output += `Skipped ${skipped.length} slot(s):\n${skipped.join('\n')}\n`;
  }
  if (updated.length === 0 && skipped.length === 0) {
    output = 'No slots to refresh.';
  }

  return { exitCode: 0, output };
}

